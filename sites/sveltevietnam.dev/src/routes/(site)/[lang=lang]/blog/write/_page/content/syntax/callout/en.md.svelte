### Callout

Callout enhances a text content with some contextual meaning and helps draw user's attention.
The following example shows one possible variant of callout:

<div class="c-callout c-callout--success c-callout--icon-trophy">

Content to be highlighted...

</div>

To display the above callout, you can use the following syntax:

```html title=".../vi.md.svelte"
<div class="c-callout c-callout--success c-callout--icon-trophy">

Content to be highlighted...

</div>
```

