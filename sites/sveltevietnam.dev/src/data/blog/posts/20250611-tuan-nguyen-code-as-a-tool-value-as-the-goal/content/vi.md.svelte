**Q: Gần đây anh đang làm gì?**<br />
A: Anh đang quản lý đội nhóm, lâu lâu viết blog công nghệ, học thêm C và Python, phát triển các dự án web AI với SvelteKit và duy trì một dự án React Native.

**Q: Anh debug Svelte apps như thế nào?**<br />
A: `console.log` thôi, không gì đặc biệt cả – đơn giản mà hiệu quả.

**Q: Dev setup của anh gồm những gì?**<br />
A: MacOS, VSCode với SublimeText keymap, pnpm và hệ sinh thái Cloudflare.

**Q: Khó khăn khi học Svelte?**<br />
A: Chắc là đoạn chuyển từ Svelte 4 sang Svelte 5, đặc biệt là `@render`. Mất 2 ngày mới xong.

**Q: Từ khi làm CTO, cách anh chọn tech stack có thay đổi không?**<br />
A: Anh luôn xem xét tài nguyên sẵn có của công ty. Kiến trúc là thứ cần quan tâm nhất, chứ không phải chọn công nghệ nào đang hot.

**Q: Là người đang quản lý cộng đồng React Native với hơn 30,000 thành viên, anh có quan điểm gì về xây dựng cộng đồng?**<br />
A: Với anh, đó là hiệu ứng "tuyết lăn" – một hành động tích cực nhỏ có thể tạo ra tác động lớn dần theo thời gian.

> Cùng nhau trải nghiệm và đóng góp, ban đầu có thể nhỏ lẻ, sẽ làm cộng đồng mạnh hơn. Cứ thế, ta cùng nhau phát triển.

**Q: Anh muốn thấy thêm hoạt động gì từ cộng đồng Svelte Việt Nam?**<br />
A: Anh rất muốn có gặp gỡ hàng tháng, một không gian cởi mở, chân thật, nơi mọi người chia sẻ điều mình học được. Biết đâu hành trình của người này lại truyền cảm hứng cho dự án tiếp theo của người khác.

