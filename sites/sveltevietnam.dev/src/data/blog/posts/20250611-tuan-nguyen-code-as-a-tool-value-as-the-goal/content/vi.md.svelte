## Hỏi nhanh đáp gọn

**Q: Anh biết đến Svelte và cộng đồng này như thế nào?**<br />
A: Tình cờ anh xem một video về Svelte trên kênh Fireship. Anh bị ấn tượng bởi lý thuyết đằng sau cách hoạt động của Svelte và lý do tại sao nó nhanh hơn so với React hay Vue.

**Q: Nếu dùng một emoji để mô tả hành trình với Svelte thì đó là gì? Vì sao?**<br />
A: 🌟 – Ngôi sao sáng, vì nó mở mang tư duy của anh về phát triển web, về trình duyệt và những cơ hội mới. Nhờ Svelte, anh bắt đầu làm việc nhiều hơn với native Web APIs. Trước đây khi dùng React, những API này thường bị “đóng hộp” bên trong framework, kéo lập trình viên ra xa Web APIs. Rồi cứ phải chạy theo mấy thứ đặc trưng của React – đúng là điên thật.

